-- Files: workspace-based isolation (workspace members see all workspace files)
CREATE POLICY "files_workspace_isolation" ON public.files
  FOR ALL USING (
    workspace_id = public.current_workspace_id()
  );

-- Quotas: workspace-based isolation
CREATE POLICY "quotas_workspace_isolation" ON public.workspace_storage_quotas
  FOR SELECT USING (
    workspace_id = public.current_workspace_id()
  );
